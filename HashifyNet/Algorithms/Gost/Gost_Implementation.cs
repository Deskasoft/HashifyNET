	/// Represents the implementation of the GOST cryptographic hash function.
	/// </summary>
	/// <remarks>This class provides an implementation of the GOST hash function, which is a cryptographic hash
	/// function used for generating fixed-size hash values from arbitrary input data. It supports both 256-bit and 512-bit
	/// hash sizes, and it is designed to be used in cryptographic applications where data integrity and security are
	/// critical.</remarks>
